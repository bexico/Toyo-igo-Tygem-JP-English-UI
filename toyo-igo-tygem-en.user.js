  // ── fetch() interception (gibo_load uses fetch, not XHR) ─────────────────
  const origFetch = window.fetch;
  window.fetch = function (input, init) {
    const url = typeof input === 'string' ? input
              : (input && input.url) ? input.url
              : '';

    if (!url.includes('gibo_load')) {
      return origFetch.apply(this, arguments);
    }

    return origFetch.apply(this, arguments).then(function (response) {
      return response.text().then(function (text) {
        const patched = patchGibo(text);
        console.log('[toyo-translate] gibo patched successfully');
        return new Response(patched, {
          status:     response.status,
          statusText: response.statusText,
          headers:    response.headers,
        });
      });
    });
  };

  // ── XHR interception (for lang.xml) ──────────────────────────────────────

  const NativeOpen = XMLHttpRequest.prototype.open;
  const NativeSend = XMLHttpRequest.prototype.send;

  XMLHttpRequest.prototype.open = function (method, url) {
    this._toyoPatch = (typeof url === 'string' && url.includes('lang.xml'))
      ? 'lang'
      : null;
    return NativeOpen.apply(this, arguments);
  };

  XMLHttpRequest.prototype.send = function () {
    if (!this._toyoPatch) {
      return NativeSend.apply(this, arguments);
    }

    const xhr = this;
    let appHandler = null;
    let patched = false;

    function applyPatch() {
      if (patched) return;
      patched = true;
      try {
        const rawText = Object.getOwnPropertyDescriptor(
          XMLHttpRequest.prototype, 'responseText'
        ).get.call(xhr);

        const patchedText = patchLangXml(rawText);

        Object.defineProperty(xhr, 'responseText', {
          value: patchedText, writable: false, configurable: true
        });
        Object.defineProperty(xhr, 'response', {
          value: patchedText, writable: false, configurable: true
        });

        const parser = new DOMParser();
        const xmlDoc = parser.parseFromString(patchedText, 'application/xml');
        Object.defineProperty(xhr, 'responseXML', {
          value: xmlDoc, writable: false, configurable: true
        });

        console.log('[toyo-translate] lang.xml patched successfully');
      } catch (e) {
        console.warn('[toyo-translate] patch error:', e);
      }
    }

    Object.defineProperty(xhr, 'onreadystatechange', {
      set: function (fn) { appHandler = fn; },
      get: function (){ return appHandler; },
      configurable: true
    });

    xhr.addEventListener('readystatechange', function () {
      if (xhr.readyState === 4) {
        applyPatch();
        if (appHandler) {
          try { appHandler.call(xhr); } catch (e) {}
        }
      }
    }, true);

    return NativeSend.apply(this, arguments);
  };


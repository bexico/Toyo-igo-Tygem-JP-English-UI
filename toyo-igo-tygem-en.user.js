  // ── Patch functions ───────────────────────────────────────────────────────

  function escapeXml(str) {
    return str
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;');
  }

  function patchLangXml(xml) {
    return xml.replace(
      /<xlang>[\s\S]*?<\/xlang>/g,
      function (block) {
        const keyMatch = block.match(/<xkey>([^<]+)<\/xkey>/);
        if (!keyMatch) return block;
        const key = keyMatch[1].trim();
        if (!(key in T)) return block;
        return block.replace(
          /<xtext>[^<]*<\/xtext>/,
          '<xtext>' + escapeXml(T[key]) + '</xtext>'
        );
      }
    );
  }

  function patchGibo(text) {
    try {
      const data = JSON.parse(text);
      if (data.list) {
        data.list = data.list.map(function (r) {
          if (r.CateName) {
            r.CateName = r.CateName
              // Game type (must come first)
              .replace(/昇降級/g, 'Ranked')
              .replace(/親善/g, 'Friendly')
              // Handicap — specific patterns before generic ones
              .replace(/(\d+)目置碁/g, '$1s Hcap') // e.g. 4目置碁 → 4s Hcap
              .replace(/(\d+)目半出し/g, '$1.5 komi') // e.g. 6目半出し → 6.5 komi
              .replace(/(\d+)目出し/g, '$1 komi') // e.g. 6目出し → 6 komi
              .replace(/(\d+)目/g, '$1s') // e.g. 3目 → 3s
              .replace(/(\d+)子/g, '$1s') // e.g. 4子 → 4s
              // Even / fixed colour
              .replace(/互先/g, 'Even')
              .replace(/定先/g, 'Fixed')
              // Komi / other
              .replace(/コミない/g, 'No komi')
              .replace(/置碁/g, 'Hcap')
              // Clean up leftover Japanese punctuation
              .replace(/　/g, ' ') // full-width space
              .trim();
          }
          return r;
        });
      }
      return JSON.stringify(data);
    } catch (e) {
      return text;
    }
  }

